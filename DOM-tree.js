/* 
DOM tree
=> konsep yang menggambarkan bahwa setiap isi didalam document.html baik itu elemen, attribut, dan text didalamnya merupakan satu kesatuan. digambarkan layaknya silsilah didalam keluarga.
simpul simpul didalam silsilah keluarga, disebut dengan node.
node terdiri dari beberapa jenis:
1. document
2. element
3. text
dll.

gabungan dari beberapa node dalam satu bagian silsilah deisebut dengan nodelist.
node list yang terdiri dari node bertipe element disebut dengan HTML colection.
nodelist dapat berisi node jenis apapun. sebaliknya, HTML collection terdiri dari node bertipe element.
html collection bersifat live, dan node list tidak.
keduanya kembar tapi tak sama dengan data array.

hirarki didalam DOM tree :
1. root node : merupakan akar dari silsilah DOM tree dalam hal ini document.html
2. parent node : node yang berada satu tingkat diatas node lain didalam DOM Tree.
3. sibling node : node yang berada satu tingkat dengan node lain didalam DOM Tree.
4. child node : node yang berada satu tingkat dibawah node lain didalam DOM Tree.
5. grand parent node : node yang berada 2 tingkat diatas node lain
6. grand child node : node yang berada 2 tingkat dibawah node lain
7. ancestor node : node yang berada lebih dari 2 tingkat ke atas dari node lain.
8. decendant node : node yang berada lebih dari 2 tingkat kebawah dari node lain.
*/
